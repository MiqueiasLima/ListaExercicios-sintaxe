package exercicios;

public class Numeros150a300 {
    public static void main(String[] args) {

        int numInicial;
        int numFinal = 300;

        for (numInicial = 150; numInicial <= numFinal; numInicial++) {
            System.out.println(numInicial);
        }
        System.out.println(numInicial);

    }
}
